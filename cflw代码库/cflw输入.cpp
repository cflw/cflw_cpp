C按键组::C按键组(t数量 a数量):
	m分配数量(a数量), 
	m使用数量(a数量),
	m这次(new bool[a数量]),
	m上次(new bool[a数量]) {
}
C按键组::~C按键组() {
	delete[] m这次;
	delete[] m上次;
}
void C按键组::f重置数量(t数量 n) {
	if (m分配数量 < n) {
		f重分配(m这次, n);
		f重分配(m上次, n);
		m分配数量 = n;
	}
	m使用数量 = n;
}
t数量 C按键组::fg数量() const {
	return m使用数量;
}
S按键 C按键组::f按键(t索引 i) const {
	assert(i < m分配数量);
	return S按键{m这次[i], m上次[i]};
}
void C按键组::f覆盖上次() {
	memcpy(m上次, m这次, m使用数量);
}
void C按键组::f清空上次() {
	memset(m上次, 0, m使用数量);
}
void C按键组::f清空这次() {
	memset(m这次, 0, m使用数量);
}
