std::tuple<bool, const S按键 &> C互斥键::f计算(const S按键 &a正, const S按键 &a反) {
	if (a正.f刚按下()) {
		m键 = true;
		return {m键, a正};
	} else if (a反.f刚按下()) {
		m键 = false;
		return {m键, a反};
	}
	const S按键 &v引用 = (m键 ? a正 : a反);
	return {m键, v引用};
}
