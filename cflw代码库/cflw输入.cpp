C按住重复::C按住重复(float a延迟, float a间隔):
	m延迟(a延迟), m间隔(a间隔) {
}
void C按住重复::fs重置时间(float a延迟, float a间隔) {
	m次数 = 0;
	m时间 = 0;
	m延迟 = a延迟;
	m间隔 = a间隔;
}
bool C按住重复::f滴答(const S按键 &a按键, float a时间) {
	if (a按键.f刚按下()) {
		m次数 = 0;
		m时间 = 0;
		return true;
	} else if (a按键.f按下()) {
		auto f比较 = [this](float a间隔) {
			if (m时间 >= a间隔) {
				++m次数;
				m时间 = 0;
				return true;
			} else {
				return false;
			}
		};
		m时间 += a时间;
		if (m次数) {
			return f比较(m间隔);
		} else {
			return f比较(m延迟);
		}
	} else {
		return false;
	}
}
