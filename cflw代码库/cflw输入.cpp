S按键::S按键(bool a这次, bool a上次):
	m这次(a这次),
	m上次(a上次) {
}
S按键::operator bool() const {
	return m这次;
}
bool S按键::f刚按下() const {
	return m这次 && !m上次;
}
bool S按键::f刚松开() const {
	return !m这次 && m上次;
}
bool S按键::f按下() const {
	return m这次;
}
bool S按键::f松开() const {
	return !m这次;
}
bool S按键::f持续() const {
	return m这次 == m上次;
}
bool S按键::f变化() const {
	return m这次 != m上次;
}
void S按键::f覆盖上次() & {
	m上次 = m这次;
}
