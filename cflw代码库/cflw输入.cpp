C缓冲按键组::C缓冲按键组(t数量 a数量):
	m分配数量(a数量),
	m使用数量(a数量),
	m立即(new bool[a数量]), 
	m缓冲(new bool[a数量]) {
	f清空();
}
C缓冲按键组::~C缓冲按键组() {
	delete[] m立即;
	delete[] m缓冲;
}
void C缓冲按键组::f重置数量(t数量 n) {
	if (m分配数量 < n) {
		f重分配(m立即, n);
		f重分配(m缓冲, n);
		m分配数量 = n;
	}
	m使用数量 = n;
}
t数量 C缓冲按键组::fg数量() const {
	return m使用数量;
}
void C缓冲按键组::f按下(t索引 a按键) {
	m立即[a按键] = true;
	m缓冲[a按键] = true;
}
void C缓冲按键组::f松开(t索引 a按键) {
	m立即[a按键] = false;
}
void C缓冲按键组::f复制到按键(C按键组 &a按键) {
	assert(m使用数量 <= a按键.m分配数量);
	memcpy(a按键.m这次, m缓冲, m使用数量);
	memcpy(m缓冲, m立即, m使用数量);
}
void C缓冲按键组::f清空() {
	memset(m立即, 0, m使用数量);
	memset(m缓冲, 0, m使用数量);
}
//一个按键
